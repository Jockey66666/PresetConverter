// AcousticPack : Acoustic Image and Acoustic sim
func AcousticPack(fx FxElement, sigpath *[]SigpathElement, embedded *[]EmbeddedData) {
	sigpathElement := GetSigpathElement(fx)
	*sigpath = append(*sigpath, sigpathElement)

	uuid := addAcousticAmp(sigpathElement.Active, sigpath)
	addAcousticCab(sigpathElement.Active, uuid, sigpath)
	addEmbeddedData(uuid, embedded)
}

func addAcousticAmp(active bool, sigpath *[]SigpathElement) string {
	var sigpathElement SigpathElement
	sigpathElement.Active = active
	sigpathElement.AmpID = acousticAmpID
	sigpathElement.AmpType = "AmpHead"
	sigpathElement.DspID = "BiasAmp2"

	uuid := uuid.Must(uuid.NewRandom())
	sigpathElement.ID = strings.ToUpper(uuid.String())
	sigpathElement.Param = make([]SigpathParam, len(acousticAmpHeadParam))

	for i, param := range acousticAmpHeadParam {
		p := SigpathParam{
			ID:    i,
			Value: param,
		}
		sigpathElement.Param[i] = p
	}

	*sigpath = append(*sigpath, sigpathElement)
	return sigpathElement.ID
}

func addAcousticCab(active bool, uuid string, sigpath *[]SigpathElement) {
	var sigpathElement SigpathElement
	sigpathElement.Active = active
	sigpathElement.AmpID = acousticAmpID
	sigpathElement.AmpType = "AmpCab"
	sigpathElement.DspID = "BiasAmp2"
	sigpathElement.ID = uuid

	sigpathElement.Param = make([]SigpathParam, len(acousticAmpCabParam))

	for i, param := range acousticAmpCabParam {
		p := SigpathParam{
			ID:    i,
			Value: param,
		}
		sigpathElement.Param[i] = p
	}

	*sigpath = append(*sigpath, sigpathElement)
}

func addEmbeddedData(uuid string, embedded *[]EmbeddedData) {
	var data EmbeddedData
	data.EmbeddedType = "BiasAmp2"
	data.AmpID = acousticAmpID
	data.ID = uuid

	var extraData interface{}
	json.Unmarshal([]byte(embeddedData), &extraData)

	m := extraData.(map[string]interface{})

	data.AmpData = m["ampData"]
	data.MetaData = m["metaData"]
	data.PanelData = m["panelData"]

	*embedded = append(*embedded, data)
}

